package com.biju.unionpayutils;

public class Tn {
	private String tn;
	private String qyOrderNO;
	private String orderNO;
	private String sign;
	public String getTn() {
		return tn;
	}
	public void setTn(String tn) {
		this.tn = tn;
	}
	public String getQyOrderNO() {
		return qyOrderNO;
	}
	public void setQyOrderNO(String qyOrderNO) {
		this.qyOrderNO = qyOrderNO;
	}
	public String getOrderNO() {
		return orderNO;
	}
	public void setOrderNO(String orderNO) {
		this.orderNO = orderNO;
	}
	public String getSign() {
		return sign;
	}
	public void setSign(String sign) {
		this.sign = sign;
	}
	
}
